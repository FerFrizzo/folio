#!/usr/bin/env node
/* eslint-disable */
// Syncs the canonical PDF templates from src/lib/pdf/ into the functions
// package's template-snapshot directory. Rewrites imports so they resolve
// against the functions runtime (no @/ aliases, no React Native deps).
//
// Run via `npm --prefix functions run build` (which depends on this) or
// `npm --prefix functions run sync` directly.

const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..", "..");
const srcDir = path.join(repoRoot, "src", "lib", "pdf");
const outDir = path.join(repoRoot, "functions", "src", "lib", "template-snapshot");

const HEADER = `// AUTO-GENERATED by functions/scripts/sync-templates.js — do not edit by hand.
// Source: src/lib/pdf/<filename>.ts. Run \`npm --prefix functions run sync\`
// after editing the canonical template.\n\n`;

const REPLACEMENTS = [
  // Schema types live next to the snapshots in functions/src/lib/types.ts
  [
    /from "@\/src\/types\/schemas"/g,
    'from "../types"',
  ],
  // Money helpers — functions has its own copy
  [
    /from "@\/src\/lib\/money"/g,
    'from "../money"',
  ],
  // ABN helpers — same
  [
    /from "@\/src\/lib\/abn"/g,
    'from "../abn"',
  ],
  // template-free.ts imports template-pro.ts via relative path; in the snapshot
  // directory the file is named invoice-pro.ts.
  [
    /from "\.\/template-pro"/g,
    'from "./invoice-pro"',
  ],
];

const FILES = [
  { src: "template-pro.ts",         out: "invoice-pro.ts"  },
  { src: "template-free.ts",        out: "invoice-free.ts" },
  { src: "credit-note-template.ts", out: "credit-note.ts"  },
];

function transform(srcPath, outPath) {
  const raw = fs.readFileSync(srcPath, "utf8");
  let out = raw;
  for (const [pattern, replacement] of REPLACEMENTS) {
    out = out.replace(pattern, replacement);
  }
  return HEADER + out;
}

function main() {
  fs.mkdirSync(outDir, { recursive: true });
  for (const { src, out } of FILES) {
    const srcPath = path.join(srcDir, src);
    const outPath = path.join(outDir, out);
    const content = transform(srcPath, outPath);
    fs.writeFileSync(outPath, content);
    console.log(`synced ${src} → ${path.relative(repoRoot, outPath)}`);
  }
}

if (require.main === module) main();

module.exports = { transform, FILES, srcDir, outDir };
